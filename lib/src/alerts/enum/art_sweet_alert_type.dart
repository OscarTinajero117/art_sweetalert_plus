/// Enum for the type of the alert
enum ArtSweetAlertPlusType {
  /// Success alert, typically used to show a positive outcome or confirmation.
  success,

  /// Warning alert, usually used to inform the user about a potential issue or danger.
  warning,

  /// Error alert, commonly used for displaying errors or failure messages.
  danger,

  /// Question alert, typically used when you need user confirmation or to ask a question.
  question,

  /// Info alert, used to display informational messages to the user.
  info,
}
